    integ_fail_o = 1'b0;
    alert_o      = 1'b0;
    send_init    = 1'b0;
    
    send_ping    = ping_rise;
    unique case (state_q)
      Idle: begin
        
        if (alert_level) begin
          state_d = HsAckWait;
          ack_pd  = 1'b1;
          
          if (ping_pending_q) begin
            ping_ok_o = 1'b1;
          end else begin
            alert_o   = 1'b1;
          end
        end
      end
      
      HsAckWait: begin
        if (!alert_level) begin
          state_d  = Pause0;
        end else begin
          ack_pd = 1'b1;
        end
      end
      
      Pause0: state_d = Pause1;
      Pause1: state_d = Idle;
      
      
      InitReq: begin
        
        send_init = 1'b1;
        
        send_ping = 1'b0;
        
        
        
        
        if (mubi4_test_true_strict(init_trig_i)) begin
          ping_ok_o = ping_pending_q;
        
        
        end else if (alert_sigint) begin
          state_d = InitAckWait;
        end
      end
      
      
      
      InitAckWait: begin
        
        send_ping = 1'b0;
        if (!alert_sigint) begin
          state_d = Pause0;
          
          
          
          send_ping = ping_rise || ping_pending_q;
        end
      end
      default: state_d = Idle;
    endcase
    
    
    if (!(state_q inside {InitReq, InitAckWait})) begin
      
      if (mubi4_test_true_strict(init_trig_i)) begin
        state_d      = InitReq;
        ack_pd       = 1'b0;
        ping_ok_o    = 1'b0;
        integ_fail_o = 1'b0;
        alert_o      = 1'b0;
        send_init    = 1'b1;
      
      
      end else if (alert_sigint) begin
        state_d      = Idle;
        ack_pd       = 1'b0;
        ping_ok_o    = 1'b0;
        integ_fail_o = 1'b1;
        alert_o      = 1'b0;
      end
    end
  end
  always_ff @(posedge clk_i or negedge rst_ni) begin : p_reg
    if (!rst_ni) begin
      
      
      state_q        <= InitReq;
      ping_req_q     <= 1'b0;
      ping_pending_q <= 1'b0;
    end else begin
      state_q        <= state_d;
      ping_req_q     <= ping_req_d;
      ping_pending_q <= ping_pending_d;
    end
  end
  
  
  
