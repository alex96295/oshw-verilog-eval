#(
  parameter int               Width = 2,
  
  
  parameter logic [Width-1:0] ResetValue = '0,
  
  
  parameter bit               EnableAlertTriggerSVA = 1,
  
  
  
  
  
  
  
  
  parameter action_mask_t     PossibleActions = {$bits(action_mask_t){1'b1}}
